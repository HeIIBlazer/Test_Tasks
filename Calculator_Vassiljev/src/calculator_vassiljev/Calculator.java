public class Calculator {

    public double liitmine(double a, double b){
        return a+b;
    }
    
    public double lahutamine(double a, double b){
        return a-b;
    }
    
    public double korrutamine(double a, double b){
        return a*b;
    }
    
    public double jagamine(double a, double b){
        return a/b;
    }
}
